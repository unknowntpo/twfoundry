import { ref } from 'vue';

export const SUPPORTED_LOCALES = [
  { code: 'en', label: 'EN' },
  { code: 'zh-TW', label: '繁中' },
];

const STORAGE_KEY = 'twfoundry.locale';
const browserLocale = globalThis.navigator?.language === 'zh-TW' ? 'zh-TW' : 'en';
const storedLocale = globalThis.localStorage?.getItem(STORAGE_KEY);

export const locale = ref(
  SUPPORTED_LOCALES.some((option) => option.code === storedLocale)
    ? storedLocale
    : browserLocale,
);

export function setLocale(nextLocale) {
  if (!SUPPORTED_LOCALES.some((option) => option.code === nextLocale)) return;
  locale.value = nextLocale;
  globalThis.localStorage?.setItem(STORAGE_KEY, nextLocale);
}

